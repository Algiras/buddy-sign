(def ^{:private true} cofactor 1)

(defn ^{:private true} build-curve [finite-field f-coeff s-coeff gen-x gen-y order-n]
  (new ECParameterSpec
       (new EllipticCurve
            (new ECFieldFp (new BigInteger finite-field))
            (new BigInteger f-coeff)
            (new BigInteger s-coeff))
       (new ECPoint (new BigInteger gen-x) (new BigInteger gen-y))
       (new BigInteger order-n)
       cofactor))

